//Bitwise Or operator
public class L20BitwiseOrAnd
{
	public static void main(String[] args)
	{
		int i=22;//10110
		int j=15;//+1111
				//by Or operations-> 11111 i.e 31
		i=i|j;
		System.out.println(i);
	}
}

