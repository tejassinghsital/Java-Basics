//Store values in variable for later use

class L8Variables1
{
	public static void main(String[] args)
	{
		int num1=3;
		int num2=5;
		System.out.println(num1+num2);
	}
}

//Later use example with variables

class L8Variables2
{
	public static void main(String[] args)
	{
		int num3=3;
		int num4=5;
		num4=6;
		System.out.println(num3+num4);
	}
}