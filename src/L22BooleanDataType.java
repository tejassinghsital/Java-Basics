// Boolean Data Type
public class L22BooleanDataType
{
	public static void main(String[] args)
	{
		boolean b=true;// boolean with small b is DatatType and Boolean with capital B is wraper class
		System.out.println(b);
	}
}

// Reversing a Boolean

class L22BooleanDataType1
{
	public static void main(String[] args)
	{
		boolean c=true;
		c=!c;
		System.out.println(c);
	}
}